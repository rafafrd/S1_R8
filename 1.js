// WHILE
let i = 6;
if (!isNaN(i)) {
    while (i <= 12) {
        console.log(i);
        i++;
    }
}
console.log("FIM");

// FOR
for (let j = 6; j <= 12; j++) {
    if (!isNaN(j)) console.log(j);
}
console.log("FIM");

// DO-WHILE
let k = 6;
if (!isNaN(k)) {
    do {
        console.log(k);
        k++;
    } while (k <= 12);
}
console.log("FIM");